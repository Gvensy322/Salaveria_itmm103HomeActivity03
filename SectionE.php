<h1>PHP 5 Variables</h1>

<h>Variables are "containers" for storing information.</p>

<h2>PHP Variables</h2>

<p>A variable can have a short name (like x and y) or a more descriptive name (age, carname, total_volume).</p>

<p>Rules for PHP variables:</p>
<p>A variable starts with the $ sign, followed by the name of the.</p>
<p>A variable name must start with a letter or the underscore character.</p>
<p>A variable name cannot start with a number.</p>
<p>A variable name can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ ).</p>
<p>Variable names are case-sensitive ($age and $AGE are two different variables).</p>
<p>Remember that PHP variable names are case-sensitive!</p>


