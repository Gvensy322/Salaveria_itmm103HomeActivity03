<h1>PHP 5 Constants</h1>

<p>Constants are like variables except that once they are defined they cannot be changed or undefined.</p>

<h2>PHP Constants</h2>


<p>A constant is an identifier (name) for a simple value. The value cannot be changed during the script.</p>
<p>A valid constant name starts with a letter or underscore (no $ sign before the constant name).</p>
<p>Note: Unlike variables, constants are automatically global across the entire script.
</p>


